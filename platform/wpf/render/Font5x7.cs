namespace wpf.render
{
    public static class Font5x7
    {
        public static ulong Get(char c)
        {
            if (c >= 'A' && c <= 'Z')
                return Uppercase[c - 'A'];

            if (c >= 'a' && c <= 'z')
                return Lowercase[c - 'a'];

            if (c >= '0' && c <= '9')
                return Digits[c - '0'];

            if (c == ' ')
                return 0;

            if (c == ':')
                return COLON;

            throw new ArgumentException($"지원하지 않는 문자: {c}");
        }

        public static readonly ulong COLON = 0b_00000_00100_00000_00000_00100_00000_00000;

        // ===============================
        // A–Z
        // ===============================
        public static readonly ulong[] Uppercase =
